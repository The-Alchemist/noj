(ns noj-book.recommended-libraries)

;; We recommend combining Noj with the following libraries:
;; * [Wolframite](https://scicloj.github.io/wolframite/) - Wolfram Language bindings
;; * [kixi.stats](https://github.com/MastodonC/kixi.stats) - statistics with transducers
;; * [fastmath-clustering](https://generateme.github.io/fastmath-clustering/) - a wrapper for [Smile](https://haifengl.github.io/) 2 clustering algorithms
;; * [scicloj.ml.smile](https://github.com/scicloj/scicloj.ml.smile) - Smile 2 models for [metamorph.ml](https://github.com/scicloj/metamorph.ml)
;; * [clj-djl](https://github.com/scicloj/clj-djl) - bindings for [Deep Java Library](https://djl.ai/)
;; * [scicloj.ml.clj-djl](https://github.com/scicloj/scicloj.ml.clj-djl) - clj-djl models for [metamorph.ml](https://github.com/scicloj/metamorph.ml)
;; * [Neanderthal](https://neanderthal.uncomplicate.org/) - high-performance numerical computing
;; * [deep-diamond](https://github.com/uncomplicate/deep-diamond) - tensors & deep learning
;; * [llama.clj](https://github.com/phronmophobic/llama.clj) - running LLMs locally
;; * [Bosquet](https://github.com/zmedelis/bosquet) - tooling for LLM applications
